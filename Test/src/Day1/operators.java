package Day1;

public class operators {

	public static void main(String[] args) {
		int a=25;
		int b=15;
		System.out.println("a + b = "+(a+b));
		System.out.println("a - b = "+(a-b));
		System.out.println("a * b = "+(a*b));
		System.out.println("a / b = "+(a/b));
		System.out.println("a % b = "+(a%b));

	}

}
